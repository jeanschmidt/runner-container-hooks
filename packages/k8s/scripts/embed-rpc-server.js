#!/usr/bin/env node
// Embed pre-built static rpc-server binaries (amd64 + arm64) into a TS module
// exported as base64 strings. The binaries are produced by
// scripts/build-rpc-server.sh and live in packages/k8s/rpc-server/binaries/.
//
// To update: edit rpc-server/src/main.rs, run scripts/build-rpc-server.sh,
// then run this script.

const fs = require('fs')
const path = require('path')

const binDir = path.join(__dirname, '..', 'rpc-server', 'binaries')
const tsPath = path.join(__dirname, '..', 'src', 'k8s', 'rpc-server-script.ts')

const amd64Path = path.join(binDir, 'rpc-server-amd64')
const arm64Path = path.join(binDir, 'rpc-server-arm64')

for (const p of [amd64Path, arm64Path]) {
  if (!fs.existsSync(p)) {
    console.error(
      `Missing binary: ${p}\n` +
        `Run packages/k8s/scripts/build-rpc-server.sh first.`
    )
    process.exit(1)
  }
}

const amd64 = fs.readFileSync(amd64Path).toString('base64')
const arm64 = fs.readFileSync(arm64Path).toString('base64')

const tsContent = `// Auto-generated by scripts/embed-rpc-server.js — do not edit directly.
// To update: edit rpc-server/src/main.rs, run scripts/build-rpc-server.sh,
// then run scripts/embed-rpc-server.js.
//
// Static musl binaries embedded as base64 strings. Selected at runtime by
// rpc.ts based on the target pod's architecture.
/* eslint-disable */
// prettier-ignore
export const RPC_SERVER_AMD64 = ${JSON.stringify(amd64)}
// prettier-ignore
export const RPC_SERVER_ARM64 = ${JSON.stringify(arm64)}
`

if (process.argv.includes('--verify')) {
  const existing = fs.existsSync(tsPath) ? fs.readFileSync(tsPath, 'utf-8') : ''
  if (existing !== tsContent) {
    console.error(
      "rpc-server-script.ts is stale. Run 'node scripts/embed-rpc-server.js' to regenerate."
    )
    process.exit(1)
  }
  process.exit(0)
}

fs.writeFileSync(tsPath, tsContent)
console.log(
  `wrote ${tsPath}: amd64=${(amd64.length * 0.75) | 0}B arm64=${(arm64.length * 0.75) | 0}B (decoded)`
)
